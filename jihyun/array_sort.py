# [문제]
# 정수로 이루어진 배열 arr가 주어질 때,
# 해당 배열을 오름차순으로 정렬한 결과를 반환한다.

# [접근 방식]
# - 이 문제는 '정렬' 문제이므로, 오름차순으로 정렬된 결과를 반환해야 한다.
# - 파이썬에서는 빠르고 안정적인 내장 정렬 함수(sorted)를 제공하므로 이를 사용했다.

# [함수를 사용한 이유]
# - 이 문제는 '배열(arr)을 입력으로 받아 정렬된 결과를 반환'하는 문제이다.

# [함수란?]
# - 함수는 '정해진 일을 해주는 코드 묶음(기계)'이다.
# - 입력값을 받아서(input),
#   그 값을 처리한 뒤(output),
#   결과를 반환(return)하는 역할을 한다.
# - 여기서 solution 함수는
#   '정수 배열 arr를 받아 오름차순으로 정렬한 배열을 반환하는 역할'을 한다.

# [사용한 알고리즘]
# - Python 내장 정렬(sorted)
# - 내부적으로는 Timsort라는 정렬 알고리즘을 사용
#   (삽입 정렬 + 병합 정렬 기반, 평균/최악 모두 빠름)

def solution(arr):
    return sorted(arr)
